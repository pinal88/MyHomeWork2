package myhomeworkpackage2;

public class MyHomeWorkClass6 {
    public static void main(String[] args) {
        int q=60;
        int r=20;
        //addition
        System.out.println(q+r); // =60+20 =80

        //Subtraction
        System.out.println(q-r); // =60-20 =40

        //multiplication
        System.out.println(q*r); // =60*20 =1200

        //division
        System.out.println(q/r); // =60/20 =3

        //increment operator
        System.out.println(q++); // 60 60+1 =60
        System.out.println(++q); // 61+1 60 =62

        System.out.println(r++); // 20 20+1 =20
        System.out.println(++r); // 21+1 20 =22


        //decrement operator
        System.out.println(r--); // 22 22-1 =22
        System.out.println(--r); // 21-1 20 =20

        System.out.println(q--); // 62 62-1 =62
        System.out.println(--q); // 61-1 60 =60



    }
}
